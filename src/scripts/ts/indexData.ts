// 点击向左切换商品
const pre_next = document.querySelector(".pre_next");
pre_next?.addEventListener("click", preNextHandler);

function preNextHandler(e: any) {
  if (e.target.nodeName !== "SPAN") return;
  var left: number = Number(purchase!.style.left.slice(0, -2));
  var ulWidth: number = Number(purchase!.style.width.slice(0, -2));
  if (pre_next!.children[0] === e.target) {
    // 点击向左

    left += 248 * 2
    if (left <= 239) {
      purchase!.style.left = left + "px";
    } else {
      purchase!.style.left = 239 + "px";
    }
    // console.log(purchase!.style.left)
  } else if (pre_next!.children[1] === e.target) {
    // 点击向右
    left -= 248 * 2
    if (left >= 248 * 5 - ulWidth) {
      purchase!.style.left = left + "px";
    } else {
      purchase!.style.left = 248 * 5 - 10 - ulWidth + "px";
    }
    // console.log(purchase!.style.left)
  }
}